                        <h4 class="title">HISTORIQUE DE LA FEDERATION ROYALE MAROCAINE DE SAMBO ET TAI JITSU</h4>
                        
                    </div><!-- Post Block Head End -->
                    
                    <!-- Post Block Body Start -->
                    <div class="body">
                       
                        <b>
                            La naissance d’une nouvelle fédération chargée de gérer le Tai Jitsu
                            et le Sambo témoigne de l’importance grandissante de ces deux disciplines.
                            Gérées initialement en Comités Nationaux sous la tutelle d’une autre
                            fédération, l’indépendance de ces deux sports s’est avérée impérative.
                        </b><hr>

                        <b>
                            C’est ainsi qu’en Avril 2003, en raison de la notoriété du Tai Jitsu et
                            du Sambo, du nombre de clubs et également en raison d’une cohabitation
                            devenue impossible avec l’ancienne fédération, et après plusieurs réunions
                            avec les instances responsables de l’autorité de tutelle, convaincues de la
                            nécessité de donner une suite favorable aux demandes incessantes et
                            justifiées des requérants, et conformément aux lois et règlements régissant
                            le sport national, notamment les dispositions de l’article 4 de la loi 6-87
                            relative à l’éducation physique et aux sports., il a été créé, sous la
                            dénomination de « FEDERATION ROYALE MAROCAINE DE SAMBO ET TAI
                            JITSU» (FRMTSJ), une institution à laquelle a été confiée la gestion des
                            deux disciplines.
                        </b><hr>

                        <b>
                            Les objectifs essentiels de la politique sportive de la Fédération visent
                            principalement, le développement et la promotion du Tai Jitsu et du Sambo
                            par le biais de différentes actions ponctuelles et permanentes, et la
                            formation des initiateurs régionaux qui auront la charge d’animer les clubs
                            affiliés à la FRMTSJ.
                        </b><hr>

                        <b>
                            Actuellement 45 clubs sont affiliés à la FRMTSJ, avec 2500 adhérents.
                        </b><hr>


                        <b>
                            <u>Dates clés :</u>
                        </b>
                        <nav>
                            <li><b>1980 : Introduction du SAMBO au MAROC.</b></li>
                            <li><b>1984 : Introduction du TAI-JITSU.</b></li>
                            <li><b>2001 : Participation au jeu méditerranéenne à Seville en Espagne. Le Maroc a remporté la Coupe avec brio.</b></li>
                            <li><b>2003 : Création de la Fédération Royale Marocaine de SAMBO et TAI- JITSU présidée actuellement par Me Dalil Skalli.</b></li>
                            <li><b>2005 : Me Dalil SKALI est nommé vice président de la fédération international de TAI-JITSU.</b></li>
                            <li><b>2007 : Me Dalil SKALI est nommé vice président de la fédération international de SAMBO.</b></li>
                            <li><b>2008 : Création de la Confédération Africaine de SAMBO dont la présidence a été confiée au Maroc en la personne de Mr Dalil Skalli.</b></li>
                            <li><b>2008 : Me Dalil SKALI est nommé vice président de l’Union Arabe de Sambo.</b></li>

                            <li><b>2011 : Me Me Dalil SKALI est nommé président de la Commission de Conciliation.</b></li>
                            <li><b>2014 : Me Me Dalil SKALI est nommé président de la Commission d’admission à la FIAS.</b></li>

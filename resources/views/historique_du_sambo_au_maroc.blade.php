                            L’existence du Sambo au Maroc a connu deux périodes importantes de son histoire.
                            <nav style="margin-left: 30px;">
                                <li>une période s’étalant de 1980 jusqu’à 2003;</li>
                                <li>une période à partir de 2003;</li>

                        </b><hr>

                        <b>
                            Depuis 1980, date à laquelle le Sambo a été introduit au Maroc par Mr Dalil Skalli, pionnier de cette discipline, Maître International et Arbitre Exceptionnel, le Sambo a fait l’objet d’une importante évolution.
                        </b><hr>

                        <b>
                            Géré par un Comité National sous l’égide de la Fédération de Lutte dans un premier temps, le Sambo a connu une période de stagnation due en grande partie à des négligences des responsables de la fédération de l’époque. C’est grâce à Me Dalil Skalli que le Sambo a pu traverser cette époque sans trop en souffrir.
                        </b><hr>

                        <b>
                            En effet, l’organisation des manifestations nationales (championnats, coupes et stages de formation) a permis au Sambo de se maintenir, avec en plus un évènement international important consistant en l’organisation de la coupe du monde en 1987 à Casablanca. Les athlètes marocains qui ont décroché la 3 ème place après la Russie et la Mongolie, avec deux médailles d’or et deux d’argent, ont stupéfaits les observateurs qui ont tenu à souligner le niveau technique très satisfaisant des combattants marocains. Ce résultat est dû à la témérité et à l’ambition des responsables et à leur tête Mr Dalil Skalli, à leur désir de faire du Sambo un sport égal en valeur aux autres disciplines martiales et à leur souhait d’atteindre les résultats escomptés.
                        </b><hr>


                        <b>
                            La deuxième période a débuté avec la création de la Fédération Royale Marocaine de Sambo et Tai Jitsu. En effet, après avoir mis fin à la gestion sous tutelle du Sambo, le temps était arrivé de prendre en main les destinés de notre discipline, et de tout entreprendre pour la faire rayonner au sein de la famille martiale. Et c’est chose faite. Depuis Avril 2003, le Sambo a connu une évolution continue et un développement assidu et performant, ce qui lui a permis de se faire une place honorable au sein du sport national.
                        </b><hr>

                        <b>
                            L’histoire enregistrera les efforts du staff dirigeant qui n’a ménagé aucun effort pour le bien être du Sambo aussi bien au niveau national qu’au niveau international, des efforts qui ont traversé les frontières pour le faire admettre au sein des fédérations nationales africaines et arabe, et de le rendre l’égal du Sambo des nations européennes, asiatiques et américaines.
                        </b>
                    
